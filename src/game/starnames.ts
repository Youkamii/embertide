/**
 * 절차적 명명 — 발견의 절반은 이름이다.
 *
 * 관측한 천체는 시드에서 유도된 이름과 한 줄 기록을 받는다. 우주가 결정론이므로
 * 같은 좌표의 천체는 누구에게나 같은 이름이다 — "4821, -1093 의 「재의 목걸이」를
 * 봤어?" 가 성립한다. 이름 붙일 권리가 탐험의 보상이다.
 */
import { Rng } from '../engine/rng'

const HEADS = [
  '재', '먼', '창백한', '늦은', '첫', '겹', '숨은', '기울어진', '느린', '검은',
  '유리', '북쪽', '잠든', '갈라진', '어린', '마지막', '빌린', '반쪽', '조용한', '깊은',
]
const BODIES = [
  '목걸이', '등대', '항구', '정원', '우물', '종', '문', '거울', '바늘', '요람',
  '제단', '닻', '깃털', '수레바퀴', '악보', '두레박', '탑', '피리', '그물', '벼랑',
]
const LOGS = [
  '빛이 여기서는 천천히 늙는다.',
  '아무도 부르지 않았는데 계속 울리고 있었다.',
  '가까이서 보면 전부 먼지였다. 그래서 더 놀라웠다.',
  '여기 먼저 온 것은 빛뿐이었다.',
  '돌아가는 길을 잠깐 잊고 싶어지는 곳.',
  '소리가 없는데도 낮게 웅웅거리는 기분이 든다.',
  '지도에 없다. 이제는 있다.',
  '무엇을 기다리는지 모르지만, 기다리는 자세였다.',
  '한 바퀴 도는 데 얼마나 걸릴까. 아마 내 평생보다 길 것이다.',
  '이 색의 이름을 아직 아무도 정하지 않았다.',
  '멀리서 봤을 때가 더 컸다. 마음속에서는 지금도 그렇다.',
  '연료 걱정만 없다면 영원히 볼 수 있을 것 같았다.',
]

/** 천체 이름 — 「관형어 + 명사」. 같은 시드 = 같은 이름. */
export function starName(seed: number): string {
  const r = new Rng(seed ^ 0x5eed)
  return `${HEADS[r.int(HEADS.length)]} ${BODIES[r.int(BODIES.length)]}`
}

/** 관측 기록 한 줄 — 일지에 남는 문장. */
export function starLog(seed: number): string {
  const r = new Rng(seed ^ 0x109)
  return LOGS[r.int(LOGS.length)]!
}
